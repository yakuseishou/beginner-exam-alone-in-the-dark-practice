#include <unistd.h>

void	wdm(char *s1, char *s2)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while(s2[j] && s1[i])
	{
		if (s2[j] == s1[i])
			i++;
		j++;
	}
	if (s1[i] == '\0')
		write(1, s1, i);
}

int		main(int ac, char **av)
{
	if (ac == 3)
		wdm(av[1], av[2]);
	write(1, "\n", 1);
	return (0);
}