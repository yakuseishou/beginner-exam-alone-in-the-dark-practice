#include <unistd.h>

void	aff_a(char *s)
{
	int		i;

	i = 0;
	while(s[i])
	{
		if (s[i] == 'a')
		{
			write(1, "a", 1);
			break;
		}
		i++;
	}
}

int		main(int ac, char **av)
{
	if (ac != 2)
		write(1, "a", 1);
	if (ac == 2)
		aff_a(av[1]);
	write(1, "\n", 1);
	return(0);
}
